#include <iostream>
using namespace std;
main(){
	int i,j,n;
	cout<<"enter number of lines: ";				//   1
	cin>>n;								//   22
	cout<<"The pattern is: \n";					//   333
	for(i=1;i<=n;i++){						//   4444
		for(j=1;j<=i;j++){					//   55555
			cout<<i;			       // number pattern problem
		}
		cout<<endl;
	}
	return 0;
}
